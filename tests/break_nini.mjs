let caught = 0, missed = 0;
for (const b of breaks) {
  const [label, ...rest] = b;
  let head = original.slice(0, cut), tail = original.slice(cut);
  let from, to;
  if (rest.length === 3) {           // 3引数: 文脈語の近くの from を to に置換（今回は単純化して from→to）
    [, from, to] = rest;
  } else {
    [from, to] = rest;
  }
  if (!tail.includes(from)) {
    console.error(`  ⚠️  壊し方が外れた（本文に「${from}」が無い）: ${label}`);
    missed++;
    continue;
  }
  const broken = head + tail.replace(from, to);   // 本文の最初の1件だけを壊す
  if (broken === original) { console.error(`  ⚠️  壊れていない: ${label}`); missed++; continue; }
  fs.writeFileSync(TMP, broken);
  let failed = false;
  try {
    execFileSync('node', ['tests/test_nini_article.mjs'], {
      env: { ...process.env, ARTICLE_FILE: TMP }, stdio: 'pipe',
    });
  } catch { failed = true; }
  if (failed) { console.log(`  ✓ 捕捉: ${label}`); caught++; }
  else { console.error(`  ✗ 素通し: ${label}`); missed++; }
}
fs.rmSync(TMP, { force: true });
console.log(`\n${missed ? '✗' : '✓'} 壊しテスト: ${caught}/${breaks.length} 捕捉・素通し ${missed}件`);
process.exit(missed ? 1 : 0);
