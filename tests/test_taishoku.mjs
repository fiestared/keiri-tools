/**
 * 退職金の税金（退職所得）計算機の単体テスト。
 *
 * ★ここでいちばん効いている検査は「外部オラクル」。
 *   自分の期待値どうしを比べても、実装と期待値が同じ勘違いをしていたら緑になる（gbrain §26）。
 *   だから **国税庁と自治体が“実額で”公表している計算例** を、そのまま固定して突き合わせる:
 *     - 国税庁 No.2732 例1: 退職金800万円・勤続10年2か月 → 源泉徴収税額 91,890円
 *     - 国税庁 No.2732 例2: 退職金2,300万円・勤続29年2か月 → 源泉徴収税額 380,322円
 *     - 自治体の計算例: 退職金1,042万円・勤続20年 → 市町村民税 72,600円・道府県民税 48,400円
 *   この3つが緑なら、控除・1年未満切上げ・1/2・千円未満切捨・超過累進・102.1%・
 *   100円未満切捨が **全部** 正しく噛み合っていることになる。
 */
import assert from "node:assert";
import { readFileSync } from "node:fs";
import {
  calcTaishoku, kinzokuNensu, taishokuKojo, kindOf, taxableRaw,
  shotokuzei, shotokuzeiWithFukko, floor1000, floor100, oneMonthLater,
} from "../docs/assets/taishoku_core.js";

const D = JSON.parse(readFileSync(new URL("../docs/assets/taishoku_rates_r08.json", import.meta.url)));
let n = 0;
const eq = (a, b, msg) => { assert.strictEqual(a, b, `${msg}: ${a} ≠ ${b}`); n++; };

// ===== 1. 外部オラクル：国税庁 No.2732 の計算例をそのまま =====
{
  // 例1: 退職金800万円・勤続10年2か月
  const r = calcTaishoku({ amount: 8000000, years: 10, months: 2 }, D);
  eq(r.nensu, 11, "★国税庁例1: 勤続10年2か月は11年（1年未満切上げ）");
  eq(r.kojo, 4400000, "★国税庁例1: 退職所得控除額 40万×11年＝440万円");
  eq(r.taxable, 1800000, "★国税庁例1: 課税退職所得金額（800万−440万）×1/2＝180万円");
  eq(r.incomeTax, 91890, "★★国税庁例1: 源泉徴収税額 91,890円（国税庁が公表している実額）");
}
{
  // 例2: 退職金2,300万円・勤続29年2か月
  const r = calcTaishoku({ amount: 23000000, years: 29, months: 2 }, D);
  eq(r.nensu, 30, "★国税庁例2: 勤続29年2か月は30年");
  eq(r.kojo, 15000000, "★国税庁例2: 控除 800万＋70万×10年＝1,500万円");
  eq(r.taxable, 4000000, "★国税庁例2: 課税退職所得金額 400万円");
  // 速算表なら (400万×20% − 427,500)×102.1%。ここでは89条の表を積み上げて同じ額になることを見る
  eq(r.shotoku, 372500, "★国税庁例2: 所得税額 372,500円（超過累進の積み上げ＝速算表と一致）");
  eq(r.incomeTax, 380322, "★★国税庁例2: 源泉徴収税額 380,322円（380,322.5円の1円未満を切捨）");
}

// ===== 2. 外部オラクル：住民税（自治体の公表計算例） =====
{
  // 退職金1,042万円・勤続20年 → 退職所得121万円 → 市6%＝72,600円 / 県4%＝48,400円
  const r = calcTaishoku({ amount: 10420000, years: 20, months: 0 }, D);
  eq(r.kojo, 8000000, "★住民税例: 勤続20年の控除は800万円（40万×20年）");
  eq(r.taxable, 1210000, "★住民税例: 退職所得の金額 121万円");
  eq(r.shichoson, 72600, "★★住民税例: 市町村民税 121万×6%＝72,600円");
  eq(r.dofuken, 48400, "★★住民税例: 道府県民税 121万×4%＝48,400円");
  eq(r.juminzei, 121000, "★住民税例: 住民税合計 121,000円");
}

// ===== 3. 端数の落とし方が3か所とも違う =====
eq(floor1000(1234567), 1234000, "課税退職所得金額は1,000円未満切捨");
eq(floor100(72699), 72600, "住民税は100円未満切捨");
// ★住民税は「合計してから」ではなく「別々に」切り捨てる。ここを合計で切ると答えが変わる
{
  // 課税退職所得 1,234,000円 → 市 74,040 → 74,000 / 県 49,360 → 49,300 → 合計 123,300
  // （もし合計 123,400 を切り捨てたら 123,400 になってしまう＝100円ずれる）
  const r = calcTaishoku({ amount: 10000000, years: 15, months: 0 }, D);
  const t = r.taxable;
  eq(t, 2000000, "課税退職所得金額 200万円");
  eq(r.shichoson + r.dofuken, r.juminzei, "住民税は市町村分と道府県分の合計");
  const naive = floor100((t * 10) / 100); // 合計10%を一度に切り捨てた場合
  eq(r.juminzei, 200000, "住民税 20万円（この額では別々に切っても同じ）");
  eq(naive, 200000, "（この額では差が出ない＝差が出る額は下のテストで見る）");
}
{
  // 差が出る額をわざと選ぶ: 課税退職所得 1,234,000円
  const t = 1234000;
  const sep = floor100((t * 6) / 100) + floor100((t * 4) / 100); // 74,000 + 49,300 = 123,300
  const naive = floor100((t * 10) / 100);                        // 123,400
  eq(sep, 123300, "★別々に切り捨てると 123,300円");
  eq(naive, 123400, "★合計してから切り捨てると 123,400円（100円多い＝間違い）");
  assert.notStrictEqual(sep, naive, "★この2つは一致しない（だから別々に切らないといけない）"); n++;
}

// ===== 4. 1年未満切上げ（所令69条2項）＝この計算機の見どころ =====
eq(kinzokuNensu(20, 0), 20, "勤続20年ちょうどは20年");
eq(kinzokuNensu(20, 1), 21, "★勤続20年1か月は21年（1か月でも1年に切り上がる）");
eq(kinzokuNensu(0, 3), 1, "勤続3か月は1年");
eq(kinzokuNensu(0, 0), 1, "勤続0年でも最低1年として扱う");
eq(taishokuKojo(20, false, D), 8000000, "勤続20年の控除 800万円");
eq(taishokuKojo(21, false, D), 8700000, "★勤続21年の控除 870万円（+70万円）");
{
  // ★1か月長く勤めるだけで控除が70万円増える。退職金1,500万円で税額はいくら変わるか
  const a = calcTaishoku({ amount: 15000000, years: 20, months: 0 }, D);
  const b = calcTaishoku({ amount: 15000000, years: 20, months: 1 }, D);
  eq(a.kojo, 8000000, "20年0か月の控除 800万円");
  eq(b.kojo, 8700000, "20年1か月の控除 870万円");
  eq(a.taxable, 3500000, "20年0か月の課税退職所得 350万円");
  eq(b.taxable, 3150000, "20年1か月の課税退職所得 315万円（35万円減る＝70万円の半分）");
  const diff = a.total - b.total;
  assert.ok(diff > 0, "★1か月長く勤めたほうが税金は安い"); n++;
  // 内訳: 課税退職所得が35万円減る。うち20万円が20%帯・15万円が10%帯なので
  // 所得税は 40,000+15,000＝55,000円減り、×1.021＝56,155円。住民税は 35万×10%＝35,000円減る。
  eq(a.total - b.total, 91155, "★★退職日を1か月ずらすと税額が91,155円変わる（所得税56,155＋住民税35,000）");
  const o = oneMonthLater({ amount: 15000000, years: 20, months: 0 }, D);
  eq(o.saved, 91155, "oneMonthLater が同じ差額を返す");
  eq(o.kojoUp, 700000, "oneMonthLater の控除増は70万円");
}

// ===== 5. 控除の下限80万円と障害退職の+100万円（30条6項） =====
eq(taishokuKojo(1, false, D), 800000, "勤続1年の控除は40万円ではなく80万円（下限）");
eq(taishokuKojo(1, true, D), 1800000, "★障害退職は「80万円の下限を当てた後」に+100万円＝180万円");
eq(taishokuKojo(30, true, D), 16000000, "勤続30年・障害退職 1,500万＋100万＝1,600万円");

// ===== 6. 1/2が効かない2種類（30条2項） =====
eq(kindOf(5, true), "yakuin", "役員等5年以下は特定役員退職手当等");
eq(kindOf(5, false), "tanki", "役員等以外で勤続5年以下は短期退職手当等");
eq(kindOf(6, true), "ippan", "★役員でも6年勤めれば一般（5年以下が要件）");
eq(kindOf(6, false), "ippan", "勤続6年は一般");
{
  // 同じ退職金1,000万円・勤続5年でも、一般／短期／特定役員で課税所得が変わる
  const kojo = taishokuKojo(5, false, D); // 200万円
  eq(kojo, 2000000, "勤続5年の控除 200万円");
  // 特定役員: 1/2なし → 1,000万 − 200万 ＝ 800万
  eq(taxableRaw(10000000, kojo, "yakuin", D), 8000000, "★特定役員は1/2が無い（800万円）");
  // 短期: 残額800万 > 300万 → 150万 + {1,000万 −（300万+200万）} ＝ 150万 + 500万 ＝ 650万
  eq(taxableRaw(10000000, kojo, "tanki", D), 6500000, "★短期は300万円を超える部分だけ1/2が無い（650万円）");
  // 一般: 800万 × 1/2 ＝ 400万
  eq(taxableRaw(10000000, kojo, "ippan", D), 4000000, "一般は1/2（400万円）");
}
{
  // 短期の「300万円の崖」: 残額がちょうど300万円までは1/2が効く
  const kojo = taishokuKojo(5, false, D); // 200万円
  eq(taxableRaw(5000000, kojo, "tanki", D), 1500000, "短期・残額300万円ちょうどは1/2が効く（150万円）");
  eq(taxableRaw(5010000, kojo, "tanki", D), 1510000, "★残額301万円は 150万+1万＝151万円（1万円ぶん丸ごと乗る）");
  // 一般ならこの1万円は5,000円にしかならない。短期は2倍の速さで増える
  eq(taxableRaw(5010000, kojo, "ippan", D), 1505000, "同じ額でも一般なら150.5万円");
}

// ===== 7. 税金が1円もかからない場合 =====
{
  const r = calcTaishoku({ amount: 8000000, years: 20, months: 0 }, D);
  eq(r.kojo, 8000000, "勤続20年の控除 800万円");
  eq(r.taxable, 0, "★退職金800万円＝控除800万円なら課税退職所得は0円");
  eq(r.incomeTax, 0, "所得税0円");
  eq(r.juminzei, 0, "住民税0円");
  eq(r.tedori, 8000000, "★満額そのまま手取り");
  eq(r.taxFree, true, "taxFree フラグ");
}

// ===== 8. 「退職所得の受給に関する申告書」を出していない場合（所法201条3項） =====
{
  const r = calcTaishoku({ amount: 8000000, years: 10, months: 2, filed: false }, D);
  // 控除も1/2も使わず、支給額そのものに20.42%
  eq(r.incomeTax, 1633600, "★申告書未提出なら 800万×20.42%＝1,633,600円（控除も1/2も無い）");
  const filed = calcTaishoku({ amount: 8000000, years: 10, months: 2 }, D);
  eq(filed.incomeTax, 91890, "同じ人が申告書を出していれば91,890円");
  assert.ok(r.incomeTax > filed.incomeTax * 17, "★出さないと17倍以上とられる（確定申告で取り戻せる）"); n++;
  // 住民税は申告書の有無で変わらない（地税50条の2・328条）
  eq(r.juminzei, filed.juminzei, "★住民税は申告書の提出と関係なく特別徴収される");
}

// ===== 9. 整数演算（浮動小数で1円ずれる罠）=====
eq(shotokuzeiWithFukko(372500, D), 380322, "★372,500×1.021＝380,322.5 → 380,322（切捨）");
eq(shotokuzeiWithFukko(90000, D), 91890, "90,000×1.021＝91,890（ちょうど）");
// 浮動小数で書くと 380322.49999... になり得る額でも、整数比なら落ちない
{
  const naive = Math.floor(372500 * 1.021);
  eq(naive, 380322, "（この額では浮動小数でも一致するが、整数比で書く方針は変えない）");
}

// ===== 10. 超過累進の積み上げが速算表と一致する（89条の表そのもの） =====
{
  // 速算表の控除額（195万以下:0 / 330万以下:97,500 / 695万以下:427,500 / 900万以下:636,000 /
  //                1,800万以下:1,536,000 / 4,000万以下:2,796,000 / 超:4,796,000）と一致するか
  const hayami = (t) =>
    t <= 1950000 ? t * 0.05 :
    t <= 3300000 ? t * 0.10 - 97500 :
    t <= 6950000 ? t * 0.20 - 427500 :
    t <= 9000000 ? t * 0.23 - 636000 :
    t <= 18000000 ? t * 0.33 - 1536000 :
    t <= 40000000 ? t * 0.40 - 2796000 :
                    t * 0.45 - 4796000;
  for (const t of [1000000, 1950000, 2000000, 3300000, 5000000, 6950000, 8000000, 9000000,
                   15000000, 18000000, 30000000, 40000000, 50000000]) {
    eq(shotokuzei(t, D), Math.round(hayami(t)),
      `★89条の表を積み上げた額が速算表と一致する（課税退職所得 ${t.toLocaleString()}円）`);
  }
}

