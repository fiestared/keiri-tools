// 記事「振込手数料の勘定科目」の数値を、一次ソースで確かめた事実と照合する。
//
// なぜ要るか: test_article_structure は記事の「型」を見るが、書いてある数字が真かは見ない
// (2026-07-13 第16便で、公開中の記事の税額が実際に誤っていた)。
// ここでは ①仕訳の貸借が一致すること ②一次ソースで確認した数値だけが載っていること
// ③誤りやすい旧制度の数値を断定形で書いていないこと を機械で落とす。
//
// ⚠️ 検査が対象の一部しか見ていない状態は、出力が「緑」なので正しい検査と区別がつかない
// (第18便)。そこで **見た件数を必ず assert する**。

import { readFileSync } from 'node:fs';
import assert from 'node:assert/strict';

const FILE = new URL('../docs/column/furikomi-tesuryo-kanjo-kamoku/index.html', import.meta.url);
const html = readFileSync(FILE, 'utf8');

let checks = 0;
const ok = (name) => { checks++; console.log('  ✓ ' + name); };

// ─────────────────────────────────────────────────────────────
// 1. 一次ソースで確認した事実(fixture)。ここに無い数字は記事に書かない
//    出典は記事の <h2>出典</h2> と同じ。値の根拠は右のコメント。
// ─────────────────────────────────────────────────────────────
const VERIFIED = {
  // 消費税法57条の4第3項ただし書 + 消費税法施行令70条の9第3項2号
  henkan_menjo_yen: 10000,        // 税込1万円未満の売上対価の返還 → 返還インボイス交付義務なし
  // 国税庁インボイスQ&A 問29(令和8年4月改訂)の設例
  seikyu: 10000, tesuryo: 440, furikomi: 9560,
  // 少額特例(28年改正法附則53の2)
  shogaku_kijun_oku: '1億円',      // 基準期間の課税売上高
  shogaku_tokutei: '5,000万円',    // 特定期間の課税売上高
  shogaku_kigen: '令和11年9月30日',
  // 自動販売機特例
  jihanki_yen: '3万円未満',
  // 取適法(令和7年法律第41号。2026-01-01施行) 2条8項・9項
  juugyoin_seizo: 300,            // 製造委託等: 常時使用する従業員300人超 → 300人以下
  juugyoin_joho: 100,             // 情報成果物作成委託・役務提供委託: 100人超 → 100人以下
  shihonkin: ['3億円', '1,000万円', '5,000万円'],
  shikou: '2026年1月1日',
};

// ─────────────────────────────────────────────────────────────
// 2. 仕訳の貸借一致(借方合計 == 貸方合計)
//    転記ミスは、金額を1つ打ち間違えるだけで起きる。表ごとに合計して落とす。
// ─────────────────────────────────────────────────────────────
const yen = (s) => Number(String(s).replace(/,/g, ''));
const tables = [...html.matchAll(/<table>([\s\S]*?)<\/table>/g)].map(m => m[1]);
const shiwake = tables.filter(t => /借方/.test(t) && /貸方/.test(t));

assert.equal(shiwake.length, 3, `仕訳表は3つのはず(買手/売手①/売手②)。実際=${shiwake.length}`);

shiwake.forEach((t, i) => {
  const rows = [...t.matchAll(/<tr>([\s\S]*?)<\/tr>/g)].map(m => m[1]);
  let debit = 0, credit = 0;
  for (const r of rows) {
    const cells = [...r.matchAll(/<t[dh]>([\s\S]*?)<\/t[dh]>/g)].map(m => m[1].replace(/<[^>]+>/g, '').trim());
    if (cells.length !== 4 || /借方/.test(cells[0])) continue;   // ヘッダ行はとばす
    if (/^[\d,]+$/.test(cells[1])) debit += yen(cells[1]);
    if (/^[\d,]+$/.test(cells[3])) credit += yen(cells[3]);
  }
  assert.ok(debit > 0, `仕訳表${i + 1}: 借方が読めていない(検査の正規表現を疑え)`);
  assert.equal(debit, credit, `仕訳表${i + 1}: 貸借不一致 借方=${debit} 貸方=${credit}`);
  ok(`仕訳表${i + 1} の貸借が一致 (${debit.toLocaleString()}円)`);
});

// 設例そのものの整合: 請求 - 手数料 = 振込額
assert.equal(VERIFIED.seikyu - VERIFIED.tesuryo, VERIFIED.furikomi);
ok(`設例の整合 ${VERIFIED.seikyu} − ${VERIFIED.tesuryo} = ${VERIFIED.furikomi}`);

// ─────────────────────────────────────────────────────────────
// 3. 一次ソースで確認した数値が、記事に実際に載っていること
// ─────────────────────────────────────────────────────────────
const text = html.replace(/<script[\s\S]*?<\/script>/g, '').replace(/<[^>]+>/g, ' ');

const MUST_APPEAR = [
  ['1万円未満', /1万円未満/],
  ['少額特例の基準期間1億円', /基準期間の課税売上高1億円以下/],
  ['少額特例の特定期間5,000万円', /特定期間.{0,4}5,000万円/],
  ['少額特例の期限 令和11年9月30日', /令和11年9月30日/],
  ['自動販売機特例 3万円未満', /3万円未満の自動販売機特例|自動販売機特例（3万円未満）/],
  ['取適法の施行日 2026年1月1日', /2026年1月1日/],
  ['従業員300人（製造委託等）', /従業員300人超/],
  ['従業員100人（情報成果物・役務）', /従業員100人超|100人超/],
  ['消法57条の4第3項', /57条の4第3項/],
  ['消令70条の9第3項2号', /70条の9第3項2号/],
  ['取適法5条1項3号', /5条1項3号/],
];
for (const [name, re] of MUST_APPEAR) {
  assert.ok(re.test(text), `一次ソースで確認した事実が本文に無い: ${name}`);
  ok(`本文に記載: ${name}`);
}

// ─────────────────────────────────────────────────────────────
// 4. 誤りやすい主張を断定形で書いていないこと
//    「3万円未満は領収書不要」は旧制度(インボイス開始で廃止)。肯定形で書いたら実害。
// ─────────────────────────────────────────────────────────────
assert.ok(/廃止されました/.test(text),
  '「3万円未満なら請求書等の保存不要」が廃止された旨の記述が無い');
ok('3万円未満特例の廃止に言及している');

// 「合意があれば差し引ける」を無条件の断定として書いていないか。
// 取適法の対象取引では合意があっても違反なので、必ず「対象外」の限定が付いていること。
const goui = /合意.{0,30}差し引/g;
const hits = [...text.matchAll(goui)];
assert.ok(hits.length > 0, '合意と差引の関係に触れていない');
ok(`合意と差引の関係に言及 (${hits.length}箇所)`);
// ⚠️ ここは「本文のどこかに出てくる」で見てはいけない(2026-07-13 第19便で実際に緑をすり抜けた)。
// この語はFAQの答えにも出るので、公取委の引用(callout)から核心を消しても検査が通ってしまった。
// → **位置で見る**: 公取委リーフレットを引用している callout そのものに入っていることを要求する。
const callouts = [...html.matchAll(/<div class="callout">([\s\S]*?)<\/div>/g)].map(m => m[1]);
const jftc = callouts.find(c => /トリテキ法の注意ポイント/.test(c));
assert.ok(jftc, '公取委リーフレットを引用した callout が無い');
assert.ok(/合意の有無にかかわらず/.test(jftc),
  '公取委の引用から核心「合意の有無にかかわらず」が抜けている(引用の改変は出典の捏造になる)');
ok('公取委の引用に「合意の有無にかかわらず」が入っている');

// ─────────────────────────────────────────────────────────────
// 5. 見た件数の宣言(検査が黙って部分的になっていないことの担保)
// ─────────────────────────────────────────────────────────────
const EXPECTED_CHECKS = 3 + 1 + MUST_APPEAR.length + 3;   // 仕訳3 + 設例1 + 記載11 + 断定3
assert.equal(checks, EXPECTED_CHECKS,
  `検査した件数が想定と違う (実際=${checks} 想定=${EXPECTED_CHECKS})。検査が一部しか見ていない可能性`);

console.log(`\n✅ test_furikomi_kamoku: ${checks}件すべて green (仕訳${shiwake.length}表の貸借一致を含む)`);
