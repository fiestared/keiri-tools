/**
 * 失業保険（基本手当）の計算を、**厚生労働省の公表値だけ**を使って裏取りする外部オラクル。
 *
 * なぜ要るか:
 *   test_kihonteate.mjs は自分の実装の期待値を自分で書いているだけなので、
 *   給付率の式そのものを読み違えていても気づけない。
 *   ここでは「厚労省が公表している上限額・下限額・帯ごとの基本手当日額」を正解表として、
 *   **こちらは条文（法16条＋規則28条の3）から立てた式に、公表の自動変更対象額を入れるだけ**で
 *   その表が再現できるかを見る。**別々の資料の数字が噛み合えば、読み違えていないと分かる。**
 *
 * オラクル（すべて生PDFを読んで転記）:
 *  - 厚労省「基本手当日額の計算式及び金額（令和7年8月1日～）」 https://www.mhlw.go.jp/content/001520033.pdf
 *  - 厚労省「雇用保険の基本手当日額が変更になります」LL070722保01 https://www.mhlw.go.jp/content/001520021.pdf
 */
import assert from "node:assert";
import { readFileSync } from "node:fs";
import { benefitDaily, benefitRate, wageAgeBand } from "../docs/assets/kihonteate_core.js";

const D = JSON.parse(readFileSync(new URL("../docs/assets/kihonteate_r07.json", import.meta.url)));

let n = 0;
const eq = (a, b, msg) => { assert.strictEqual(a, b, `${msg}: 実装=${a} / 厚労省=${b}`); n++; };

// ---------------------------------------------------------------------------
// 1. 基本手当日額の上限額（厚労省 LL070722保01 の表）
//    公表の「賃金日額の上限額」を入れたら、公表の「基本手当日額の上限額」が出るか。
//    上限額の賃金日額は逓減帯より上なので、率は 50%（60〜64歳は45%）になるはず。
// ---------------------------------------------------------------------------
const MAX_TABLE = [
  // [離職時の年齢, 賃金日額の上限額, 基本手当日額の上限額]
  [29, 14510, 7255],
  [30, 16110, 8055],
  [44, 16110, 8055],
  [45, 17740, 8870],
  [59, 17740, 8870],
  [60, 16940, 7623],
  [64, 16940, 7623],
];
for (const [age, wMax, yMax] of MAX_TABLE) {
  eq(D.chingin_nichigaku_max[wageAgeBand(age)], wMax, `${age}歳の賃金日額の上限額`);
  eq(benefitDaily(wMax, age, D), yMax, `${age}歳の基本手当日額の上限額`);
}

// ---------------------------------------------------------------------------
// 2. 基本手当日額の下限額（全年齢共通 2,411円）
//    厚労省の解説どおり「最低賃金日額 × 給付率80%」で出る。
// ---------------------------------------------------------------------------
eq(D.chingin_nichigaku_min, 3014, "賃金日額の下限額");
for (const age of [20, 35, 50, 62]) {
  eq(benefitDaily(3014, age, D), 2411, `${age}歳の基本手当日額の下限額`);
  eq(benefitRate(3014, age, D), 0.8, `${age}歳・下限の給付率は80%`);
}
eq(D.kihon_nichigaku_min, 2411, "データが持つ基本手当日額の下限額");

// ---------------------------------------------------------------------------
// 3. 帯ごとの基本手当日額（厚労省の計算式PDF・裏面の「給付率」表）
//    29歳以下／30〜44歳／45〜59歳 に共通の帯:
//      3,014〜5,339円 → 80%     … 2,411円〜4,271円
//      5,340〜13,140円 → 80〜50% … 4,272円〜6,570円
//      13,140円超〜上限 → 50%
// ---------------------------------------------------------------------------
for (const age of [25, 35, 50]) {
  eq(benefitDaily(3014, age, D), 2411, `${age}歳 80%帯の下端`);
  eq(benefitDaily(5339, age, D), 4271, `${age}歳 80%帯の上端（5,340円未満）`);
  eq(benefitDaily(5340, age, D), 4272, `${age}歳 逓減帯の下端`);
  eq(benefitDaily(13140, age, D), 6570, `${age}歳 逓減帯の上端`);
  eq(benefitRate(5340, age, D), 0.8, `${age}歳 逓減帯の下端の率は80%`);
  eq(benefitRate(13140, age, D), 0.5, `${age}歳 逓減帯の上端の率は50%`);
  eq(benefitRate(13141, age, D), 0.5, `${age}歳 逓減帯を超えたら一律50%`);
}

// ---------------------------------------------------------------------------
// 4. ★ 60〜64歳だけ計算式が2本ある（規則28条の3第2項のかっこ書き）
//    厚労省の公表式:
//      5,340円以上 11,800円以下 →
//        y = 0.8w − 0.35{(w−5340)/(11800−5340)}w  と  y = 0.05w + (11800 × 0.4)
//        の **いずれか低い方の額**
//      11,800円超 16,940円以下 → y = 0.45w
//    実装は規則の読み替え（率の上限）として書いているので、**公表の2本の式と一致するはず**。
// ---------------------------------------------------------------------------
const A = D.band80_upper, B = D.band_taper_upper_60_64;
let capBound = 0;
for (let w = A; w <= B; w++) {
  const linear = 0.8 * w - 0.35 * ((w - A) / (B - A)) * w; // 公表の式(1)
  const flat = 0.05 * w + B * 0.4;                          // 公表の式(2)
  const expected = Math.floor(Math.min(linear, flat));
  assert.strictEqual(benefitDaily(w, 62, D), expected,
    `60〜64歳・賃金日額${w}円: 実装=${benefitDaily(w, 62, D)} / 公表式の低い方=${expected}`);
  if (flat < linear) capBound++;
}
n++;
// かっこ書きの上限が「途中から効く」こと自体を固定する（効かないなら式は1本で足りるはず）
assert.ok(capBound > 0, "60〜64歳: かっこ書きの上限が一度も効いていない（式が2本ある意味がない）");
assert.ok(capBound < B - A, "60〜64歳: かっこ書きの上限が全域で効いている（逓減式が死んでいる）");
eq(benefitDaily(11800, 62, D), 5310, "60〜64歳・逓減帯の上端＝45%（11,800×0.45）");
eq(benefitDaily(16940, 62, D), 7623, "60〜64歳・上限額");
n++;

// ---------------------------------------------------------------------------
// 5. ★ 法律に書かれた金額は「今日の実額」ではないことの固定
//    雇用保険法16条1項・17条4項の額（平成27年度基準の原型）と、現に適用される額は違う。
//    ここが一致してしまったら、18条の自動変更を実装が無視している疑いがある。
// ---------------------------------------------------------------------------
const LAW_ORIGINAL = { band80_upper: 4920, band_taper_upper: 12090, band_taper_upper_60_64: 10880, min: 2460 };
assert.notStrictEqual(D.band80_upper, LAW_ORIGINAL.band80_upper, "80%帯の上端が条文の原型のまま");
assert.notStrictEqual(D.band_taper_upper, LAW_ORIGINAL.band_taper_upper, "逓減帯の上端が条文の原型のまま");
assert.notStrictEqual(D.chingin_nichigaku_min, LAW_ORIGINAL.min, "賃金日額の下限が条文の原型のまま");
n += 3;

// ---------------------------------------------------------------------------
// 6. 自動変更対象額は毎年8月1日に改定される（18条）。
//    データが「いつの額か」を名乗っていること、次の改定日が過ぎていないこと。
// ---------------------------------------------------------------------------
assert.match(D._meta.applies_from, /^\d{4}-08-01$/, "_meta.applies_from が8月1日でない");
assert.match(D._meta.next_revision, /^\d{4}-08-01$/, "_meta.next_revision が8月1日でない");
assert.ok(D._meta.next_revision > D._meta.applies_from, "次回改定日が適用開始日より前");
n += 3;

console.log(`✅ test_kihonteate_oracle: ${n} checks — 厚労省の公表値を、条文の式＋自動変更対象額から再現`);
