let caught = 0, missed = [];
for (const [label, mutate] of BREAKS) {
  let broken;
  try { broken = mutate(orig); }
  catch (e) { console.error(`  ⚠ ${label}: ${e.message}`); missed.push(label + '（壊し方が外れている）'); continue; }
  if (broken === orig) { missed.push(label + '（置換が効いていない）'); continue; }
  fs.writeFileSync(TMP, broken);
  const green = run(TMP);
  if (green) { console.error(`  ✗ 素通し: ${label}`); missed.push(label); }
  else { console.log(`  ✓ 捕捉: ${label}`); caught++; }
}
fs.rmSync(TMP, { force: true });

