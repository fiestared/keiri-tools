let caught = 0;
const missed = [];
for (const [name, mutate] of BREAKS) {
  let broken;
  try {
    broken = mutate(orig);
  } catch (e) {
    missed.push(`${name} … ${e.message}`);
    continue;
  }
  if (broken === orig) {
    missed.push(`${name} … 壊し方が外れている（記事が変化していない）`);
    continue;
  }
  fs.writeFileSync(TMP, broken);
  const green = run(TMP);
  fs.unlinkSync(TMP);
  if (green) missed.push(`${name} … ★素通し（嘘を注入したのに検査が緑）`);
  else caught++;
}

console.log(`壊しテスト: ${caught}/${BREAKS.length} 捕捉`);
if (missed.length) {
  console.error(`\n❌ ${missed.length}件が捕捉できていない:`);
  for (const m of missed) console.error("  - " + m);
  console.error("\n※ 素通しを見たら「検査が弱いのか、壊し方が外れたのか」を区別すること（規則8）");
  process.exit(1);
}
