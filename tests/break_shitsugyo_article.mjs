// 本文だけを狙う（headのJSON-LDに当てない）
const inBody = (html, a, b) => {
  const i = html.indexOf("<article>");
  const head = html.slice(0, i), body = html.slice(i);
  if (!body.includes(a)) throw new Error(`壊し方が外れている（本文に "${a.slice(0, 60)}" が無い）`);
  return head + body.replace(a, b);
};
// meta description / card-desc は head 側（規則9）
const inHead = (html, a, b) => {
  const i = html.indexOf("<article>");
  const head = html.slice(0, i), body = html.slice(i);
  if (!head.includes(a)) throw new Error(`壊し方が外れている（headに "${a.slice(0, 60)}" が無い）`);
  return head.replace(a, b) + body;
};

