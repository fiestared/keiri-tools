let caught = 0, missed = 0, misaimed = 0;
for (const [name, from, to] of CASES) {
  // meta は <article> より前にあるので、本文限定の breakBody では届かない → 全体を狙う
  const isMeta = name.startsWith("meta");
  let broken;
  if (isMeta) {
    broken = orig.includes(from) ? orig.replace(from, to) : null;
  } else {
    broken = breakBody(from, to);
  }
  if (broken === null || broken === orig) {
    console.log(`  ⚠ 壊し方が外れた（狙った文字列が無い）: ${name}`);
    misaimed++;
    continue;
  }
  fs.writeFileSync(TMP, broken);
  const green = run(TMP);
  fs.unlinkSync(TMP);
  if (green) {
    console.log(`  ✗ 素通し: ${name}`);
    missed++;
  } else {
    caught++;
  }
}

const total = CASES.length;
console.log(
  missed === 0 && misaimed === 0
    ? `✓ 壊しテスト: ${caught}/${total} 全て捕捉（ベースライン緑）`
    : `✗ 壊しテスト: 捕捉${caught} / 素通し${missed} / 壊し方が外れ${misaimed}（計${total}）`
);
process.exit(missed === 0 && misaimed === 0 ? 0 : 1);
