import fs from "node:fs";
import { execFileSync } from "node:child_process";

const SRC = "docs/column/saishushoku-teate/index.html";
const TMP = "docs/column/saishushoku-teate/.break.html";
const orig = fs.readFileSync(SRC, "utf8");
const cut = orig.indexOf("<article>"); // 本文の開始。head の JSON-LD を誤爆しない

const run = (file) => {
  try {
    execFileSync("node", ["tests/test_saishushoku_article.mjs"], {
      env: { ...process.env, ARTICLE_FILE: file },
      stdio: "pipe",
    });
    return true; // 緑
  } catch {
    return false; // 赤
  }
};

// ───────── 規則2: ベースライン（無傷の記事が緑か） ─────────
if (!run(SRC)) {
  console.error("✗ ベースラインが赤。無傷の記事で検査が落ちているので、壊しテストは意味を持たない。");
  console.error("  （このまま続けると「何を壊しても赤」で嘘の満点が出る。まず検査を直すこと）");
  process.exit(1);
}
console.log("  ベースライン: 無傷の記事は緑 ✓");

// 本文(<article>以降)だけを1箇所書き換える
const breakBody = (from, to) => {
  const head = orig.slice(0, cut);
  const body = orig.slice(cut);
  if (!body.includes(from)) return null; // 壊し方が外れている（狙った文字列が本文に無い）
  return head + body.replace(from, to);
};

