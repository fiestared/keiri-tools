/**
 * 傷病手当金の単体テスト。
 *
 * ★ここでいちばん効いている検査は「外部オラクル」＝**保険者が実額で公表している計算例**。
 *   自分の期待値どうしを比べても、実装と期待値が同じ勘違いをしていたら緑になる（gbrain §26）。
 *
 *   ⚠️**この検査自身がその罠を踏みかけた**（2026-07-14）:
 *   最初、上限日額 **6,667円** を「協会けんぽの公表値」としてオラクルに書いた。**嘘だった。**
 *   6,667円はどの一次情報にも載っていない。**30万円時代の派生値**にすぎず、
 *   協会けんぽの現在の額は **32万円**（支給開始日が令和7年4月1日以降）で、
 *   正しい上限日額は **7,113円**。世に出回っている「上限6,667円」は今は誤り。
 *   → **記憶にある有名な数字をオラクルとして書くな。オラクルは“取ってきた原文”だけ。**
 *
 *   本物のオラクル（公表されている計算例をそのまま固定する）:
 *     A. 協会けんぽ（12月あるケース）… 16万×6 + 18万×6 → 平均17万 → 5,670円 → **3,780円/日**
 *     B. 関東ITソフトウェア健保／厚労省事務説明会資料… 20万×5 + 24万×7 → 223,333.3
 *        → 7,440円 → **4,960円/日**
 *     C. カシオ健保… 34万×2 + 30万×10 → 306,666.66 → **10,220円**（÷30 まで。給付率は組合独自の80%）
 *     D. 厚労省 事務連絡（令和3年11月10日）… 令和4年3月4日から支給開始 → 令和5年9月3日まで **549日**
 *
 *   AとBは **丸めの向きが違う**（Aは 5,666.67 → 5,670 に切上げ、Bは 7,444.4 → 7,440 に切捨て）。
 *   2つ同時に緑になるということは、四捨五入の境界が正しいということ。
 *
 * ★丸めの「境界」を必ず踏む検査を置くこと（99条2項の端数処理は2段とも“境界は切上げ”）。
 */
