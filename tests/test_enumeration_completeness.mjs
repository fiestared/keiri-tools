/** id で名指しした要素の中身だけを取り出す（規則3: ページ全体を grep しない） */
function extractElement(html, id) {
  const open = new RegExp(`<([a-zA-Z]+)[^>]*\\bid=["']${id}["'][^>]*>`);
  const m = html.match(open);
  if (!m) return null;
  const tag = m[1];
  const start = m.index + m[0].length;
  // 同名タグのネストを数えて閉じ位置を求める
  const re = new RegExp(`<${tag}\\b[^>]*>|</${tag}>`, "g");
  re.lastIndex = start;
  let depth = 1, mm;
  while ((mm = re.exec(html))) {
    depth += mm[0].startsWith("</") ? -1 : 1;
    if (depth === 0) return html.slice(start, mm.index);
  }
  return null;
}

/** タグを剥がして「利用者に見えている文字」にする */
const visible = (h) => h.replace(/<[^>]+>/g, " ").replace(/\s+/g, " ");

let checks = 0;
const problems = [];

for (const E of ENUMERATIONS) {
  const html = await readFile(join(ROOT, E.page), "utf8");
  const el = extractElement(html, E.element);

  checks++;
  if (!el) {
    problems.push(
      `${E.page}: 列挙を載せる要素 id="${E.element}" が見つからない（${E.law}）。` +
        `画面から列挙が丸ごと消えている可能性がある。`
    );
    continue;
  }

  const text = visible(el);
  for (const item of E.items) {
    checks++;
    if (!item.must.test(text)) {
      problems.push(
        `${E.page} #${E.element}: 【${item.id} ${item.label}】が画面に出ていない（${E.law}）。\n` +
          `      ${E.why}\n` +
          `      → 法が列挙している事由を「など」で省略しないこと。省略した事由の利用者は全員、黙って誤答を受け取る。`
      );
    }
  }

  // 「など」で丸めていないか（この要素の中で列挙を締めくくる「など」は禁止）
  checks++;
  if (/(?:など|等)\s*(?:が|の)?\s*(?:理由|事由)/.test(text)) {
    problems.push(`${E.page} #${E.element}: 列挙を「など」で締めくくっている。全部書くこと。`);
  }
}

if (problems.length) {
  console.error(`✗ test_enumeration_completeness: ${problems.length}件`);
  for (const p of problems) console.error("  - " + p);
  process.exit(1);
}
console.log(`✓ test_enumeration_completeness: ${checks} checks`);
